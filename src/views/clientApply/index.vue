const rejectApply = (record) => {
  Modal.info({
    title: '提示',
    content: '确定吗？',
    simple: false,
    okText: '确定',
    cancelText: '取消',
    onOk: () => {
      api.rejectApply({
        id: record.id,
      }).then(res => {
        if (res.code == 200) {
          Message.success('操作成功')
          crudRef.value.refresh()
        } else {
          Message.error('操作失败')
        }
      })
    },
  })
  return true
}

const applySubmit = (formData) => {
